#!/bin/bash
# ============================================================
# ASC to CSV 运行脚本 (Linux/macOS)
# ============================================================

echo "========================================"
echo "  ASC to CSV 转换与可视化工具"
echo "========================================"
echo

# 检查虚拟环境
if [ ! -d "venv" ]; then
    echo "[错误] 虚拟环境不存在，请先运行 ./setup.sh"
    exit 1
fi

# 激活虚拟环境
source venv/bin/activate

# 运行程序
echo "[信息] 启动程序..."
echo
python main_app.py

echo
echo "========================================"
echo "  程序已退出"
echo "========================================"
